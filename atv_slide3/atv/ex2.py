#Juntando primeiro e segundo nome com concatenação.

#Declarando variáveis
first_name = input("Oiêe.. Informe seu nome: \n")
second_name = input("Informe seu segundo nome: \n")

#Concatenação
name_completed = first_name + " " + second_name

#Imprimindo nome completo!
print(f"Seu nome completo é {name_completed}")