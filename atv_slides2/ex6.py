# Você está no Brasil, e para temperatura usamos o grau Celsius. Porém, quando você for 
# contrato para trabalhar como programador Python no exterior, deverá usar graus Fahrenheit.

celsius = float(input("Digite a temperatura em Celsius: "))


fahrennheit = (celsius * 9/5) + 32 

res = print(f"A temperatura em Fahrenheit é: {fahrennheit}")

